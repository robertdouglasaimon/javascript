let num = [5,8,4] // Váriavel composta =D (Famoso array)
num[3] = 6 // Acrescenta o novo valor na posição parametrada nos cochetes. Caso não tenham, o JS cria a posição e adiciona o número.
num.push(7) // Metodo interno que acrescenta valores a um array.
num.length // Não é um método e sim um atributo. É o comprimento dos elementos. No nosso exemplo, o length é 2 elementos ("0,1,2").
num.sort() // Coloca a em ordem crescente os elementos do array.

console.log(`Nosso vetor é o ${num}`) 






